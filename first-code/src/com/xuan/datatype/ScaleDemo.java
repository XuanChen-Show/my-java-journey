public class ScaleDemo {
    public static void main(String[] args) {
        /*
        计算机中进制的分类 :
            十进制 : 默认
            二进制 : 0b 开头
            八进制 : 0 开头
            十六进制 : 0x 开头
        */
        System.out.println(110);
        System.out.println(0b110);
        System.out.println(0110);
        System.out.println(0x110);
    }
}
